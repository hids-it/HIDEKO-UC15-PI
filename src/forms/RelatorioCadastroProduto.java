    private void preencherTabela() {
        
                ProdutoDAO produtoDAO = new ProdutoDAO();
            
                String nome = txtNome.getText();
                List<Produto> listaProduto = produtoDAO.getProduto(nome);
                DefaultTableModel tabelaProduto = (DefaultTableModel)TbProd.getModel();
  //Limpar a tabela para preencher com os novos dados
            tabelaProduto.setNumRows(0);
                          
                TbProd.setRowSorter(new TableRowSorter(tabelaProduto));
                 
            for (Produto p : listaProduto) { //em cada volta do laço for, o mesmo adiciona uma dado(produto) dentro do objeto p
                    Object[] obj = new Object[] { 
                        p.getId(),            //id
                        p.getNomeprod(),   //nome
                        p.getDescricao(),   //descricao
                        p.getVcusto(),//vcusto
                        p.getVvenda()//vvenda
                       
                    };
                    tabelaProduto.addRow(obj);
                }    
            }





